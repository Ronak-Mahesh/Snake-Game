package com.controller;

/**
 * An interface for any object that is movable. Has one method to
 * implement: move()
 *
 * @author Ronak Mahesh - Modified
 * @see com.controller.Snake An implementation of this interface
*/

public interface Movable
{
	/**
	 * A method to be implemented in any way the user wants. Defines the way
	 * a particular object moves.
	 */
	void move();
}
